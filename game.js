// --- roll encoding ---
function encodeRoll(dice) {
    // dice: array of length 6, values 1..6
    dice = dice.slice().sort((a, b) => a - b);

    let key = 0;
    for (let i = 0; i < 6; i++) {
        key |= (dice[i] - 1) << (3 * i);
    }
    return key;
}

async function getEVData(table, bonus, rolls, id) {
    // 1. Construct the URL with your 4 integer parameters
    const url = `https://89.167.37.171?table=${table}&bonus=${bonus}&rolls=${rolls}&id=${id}`;

    try {
        // 2. Request the data from your server
        const response = await fetch(url);

        if (!response.ok) {
            throw new Error(`HTTP error! Status: ${response.status}`);
        }

        // 3. Extract the response as a raw binary ArrayBuffer
        const buffer = await response.arrayBuffer();

        // 4. Wrap the buffer in a Float32Array to read the raw bytes as floats
        const floatArray = new Float32Array(buffer);

        // 5. Convert to a regular JavaScript array (optional, but easier to work with)
        const regularArray = Array.from(floatArray);

        return regularArray;

    } catch (error) {
        console.error("Failed to fetch or parse binary float data:", error);
    }
}

